// ===== POKÉMON DATA GENERATOR =====
// Run this script ONCE with Node.js to build the pokemon-data.js file.
// It fetches data from PokeAPI and writes a static file the game can use.
//
// Usage: node tools/generate-pokemon-data.js
//
// This takes a few minutes because it makes ~2,000 API calls (with polite delays).

const MAX_POKEMON_ID = 1025; // Total number of Pokémon species
const BATCH_SIZE = 20;       // How many to fetch at once
const DELAY_MS = 500;        // Pause between batches to be nice to the API

// --- Manual flag lists ---
// These categories aren't directly available from a single API field,
// so we list the Pokémon IDs by hand.

const ULTRA_BEASTS = new Set([
    793, 794, 795, 796, 797, 798, 799, // Gen 7
    803, 804, 805, 806,                 // Gen 7 additions
]);

const PARADOX_POKEMON = new Set([
    984, 985, 986, 987, 988, 989,       // Scarlet ancient paradox
    990, 991, 992, 993, 994, 995,       // Violet future paradox
    1005, 1006,                          // Roaring Moon, Iron Valiant
    1009, 1010,                          // Walking Wake, Iron Leaves
    1022, 1023, 1024, 1025,             // DLC paradox (Iron Boulder, Gouging Fire, Raging Bolt, Iron Crown)
]);

const PSEUDO_LEGENDARIES = new Set([
    149, // Dragonite
    248, // Tyranitar
    373, // Salamence
    376, // Metagross
    445, // Garchomp
    635, // Hydreigon
    706, // Goodra
    784, // Kommo-o
    887, // Dragapult
    998, // Baxcalibur
]);

// --- Helper: sleep for a number of milliseconds ---
function sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

// --- Helper: fetch JSON from a URL ---
async function fetchJSON(url) {
    const response = await fetch(url);
    if (!response.ok) {
        throw new Error(`Failed to fetch ${url}: ${response.status}`);
    }
    return response.json();
}

// --- Fetch one Pokémon's data ---
async function fetchPokemon(id) {
    // We need two API calls per Pokémon:
    // 1. /pokemon/{id} for weight, stats
    // 2. /pokemon-species/{id} for legendary/mythical status
    const [pokemonData, speciesData] = await Promise.all([
        fetchJSON(`https://pokeapi.co/api/v2/pokemon/${id}`),
        fetchJSON(`https://pokeapi.co/api/v2/pokemon-species/${id}`),
    ]);

    // Extract the stats we need
    const stats = {};
    for (const stat of pokemonData.stats) {
        stats[stat.stat.name] = stat.base_stat;
    }

    // Build the flags array
    const flags = [];
    if (speciesData.is_legendary) flags.push("legendary");
    if (speciesData.is_mythical) flags.push("mythical");
    if (ULTRA_BEASTS.has(id)) flags.push("ultraBeast");
    if (PARADOX_POKEMON.has(id)) flags.push("paradox");
    if (PSEUDO_LEGENDARIES.has(id)) flags.push("pseudoLegendary");

    // Capitalize the name nicely
    const name = speciesData.names.find(n => n.language.name === "en")?.name
        || pokemonData.name.charAt(0).toUpperCase() + pokemonData.name.slice(1);

    return {
        id: id,
        name: name,
        weight: pokemonData.weight, // in hectograms
        attack: stats["attack"] || 0,
        spAttack: stats["special-attack"] || 0,
        speed: stats["speed"] || 0,
        hp: stats["hp"] || 0,
        flags: flags,
    };
}

// --- Main function ---
async function main() {
    console.log(`Fetching data for ${MAX_POKEMON_ID} Pokémon...`);
    console.log("This will take a few minutes. Be patient!\n");

    const allPokemon = [];
    let fetched = 0;

    for (let start = 1; start <= MAX_POKEMON_ID; start += BATCH_SIZE) {
        const end = Math.min(start + BATCH_SIZE - 1, MAX_POKEMON_ID);
        const batch = [];

        for (let id = start; id <= end; id++) {
            batch.push(fetchPokemon(id));
        }

        const results = await Promise.all(batch);
        allPokemon.push(...results);
        fetched += results.length;

        const pct = Math.round((fetched / MAX_POKEMON_ID) * 100);
        console.log(`  Fetched ${fetched}/${MAX_POKEMON_ID} (${pct}%)`);

        // Be nice to the API: pause between batches
        if (end < MAX_POKEMON_ID) {
            await sleep(DELAY_MS);
        }
    }

    // Sort by ID (should already be sorted, but just in case)
    allPokemon.sort((a, b) => a.id - b.id);

    // Build the output file
    const lines = allPokemon.map(p => {
        const flagsStr = p.flags.length > 0
            ? `[${p.flags.map(f => `"${f}"`).join(", ")}]`
            : "[]";
        return `  { id: ${p.id}, name: "${p.name}", weight: ${p.weight}, attack: ${p.attack}, spAttack: ${p.spAttack}, speed: ${p.speed}, hp: ${p.hp}, flags: ${flagsStr} },`;
    });

    const output = `// ===== ALL POKÉMON DATA =====
// This file was auto-generated by tools/generate-pokemon-data.js
// It contains info about every Pokémon so the game can check piece rules
// without making any internet requests while you play.
//
// DO NOT EDIT BY HAND. Run the generator script to rebuild this file.
// Total Pokémon: ${allPokemon.length}

const ALL_POKEMON = [
${lines.join("\n")}
];

export { ALL_POKEMON };
`;

    // Write the file
    const path = await import("path");
    const fs = await import("fs");
    const outPath = path.default.join(import.meta.dirname, "..", "pokemon-data.js");
    fs.default.writeFileSync(outPath, output, "utf-8");

    console.log(`\nDone! Wrote ${allPokemon.length} Pokémon to ${outPath}`);

    // Print some stats
    const legendaries = allPokemon.filter(p => p.flags.includes("legendary"));
    const mythicals = allPokemon.filter(p => p.flags.includes("mythical"));
    const ubs = allPokemon.filter(p => p.flags.includes("ultraBeast"));
    const paradox = allPokemon.filter(p => p.flags.includes("paradox"));
    const pseudos = allPokemon.filter(p => p.flags.includes("pseudoLegendary"));
    const heavies = allPokemon.filter(p => p.weight >= 1361 && !p.flags.includes("legendary") && !p.flags.includes("ultraBeast"));
    const bishops = allPokemon.filter(p => p.spAttack > p.attack);
    const knights = allPokemon.filter(p => p.speed > p.hp);
    const pawns = allPokemon.filter(p => p.flags.length === 0);

    console.log("\n--- Piece rule counts ---");
    console.log(`  Queens (legendary/mythical/UB/paradox): ${legendaries.length + mythicals.length + ubs.length + paradox.length} (some overlap)`);
    console.log(`  Rooks (300+ lbs, regular): ${heavies.length}`);
    console.log(`  Bishops (sp.atk > atk): ${bishops.length}`);
    console.log(`  Knights (speed > hp): ${knights.length}`);
    console.log(`  Pawns (regular): ${pawns.length}`);
    console.log(`  Kings (any): ${allPokemon.length}`);
}

main().catch(err => {
    console.error("Error:", err.message);
    process.exit(1);
});
